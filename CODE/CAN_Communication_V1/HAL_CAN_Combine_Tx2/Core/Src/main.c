/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
  HAL_CAN_Start(&hcan1);

  	  	TxHeader.DLC = 8;
  		TxHeader.RTR = CAN_RTR_DATA;
  		TxHeader.IDE = CAN_ID_STD;
  		TxHeader.StdId = 0x0A9;
  		TxHeader.ExtId = 0x0000;
  		TxHeader.TransmitGlobalTime = DISABLE;

  HAL_TIM_Base_Start(&htim2);       // Start timer for DHT timing
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)  {
	  char str[100];
	  // Read LDR sensor (ADC1 Channel 1)
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		LDR_DATA = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);

		// Read MQ5 sensor (ADC2 Channel 3)
		HAL_ADC_Start(&hadc2);
		HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
		MQ5_DATA = HAL_ADC_GetValue(&hadc2);
		HAL_ADC_Stop(&hadc2);

		 // Read DHT11 sensor
		    DHT11_Start();
		    Presence = DHT11_Check_Response();

		    Rh_byte1 = DHT11_Read();
		    Rh_byte2 = DHT11_Read();
		    Temp_byte1 = DHT11_Read();
		    Temp_byte2 = DHT11_Read();
		    SUM = DHT11_Read();

		    uint8_t check = Rh_byte1 + Rh_byte2 + Temp_byte1 + Temp_byte2;

		    if (check != SUM)
		    {
		      sprintf(str, "Checksum error!\r\n");
		      Temperature = 0;
		      Humidity = 0;
		    }
		    else
		    {
		      Humidity = Rh_byte1;     // For DHT11 decimal parts are 0
		      Temperature = Temp_byte1;
		    }

		    // Prepare CAN Tx data: 8 bytes = LDR(2) + MQ5(2) + Temp(2) + Humidity(2)
		    TxData[0] = (LDR_DATA >> 8) & 0xFF;
		    TxData[1] = LDR_DATA & 0xFF;

		    TxData[2] = (MQ5_DATA >> 8) & 0xFF;
		    TxData[3] = MQ5_DATA & 0xFF;

		    uint16_t temp_int = (uint16_t)Temperature;   // Assuming integer value for CAN
		    uint16_t hum_int = (uint16_t)Humidity;

		    TxData[4] = (temp_int >> 8) & 0xFF;
		    TxData[5] = temp_int & 0xFF;

		    TxData[6] = (hum_int >> 8) & 0xFF;
		    TxData[7] = hum_int & 0xFF;

		    if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailBox) != HAL_OK)
		    {
		      // CAN transmit error, turn on red LED
		      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
		    }
		    else
		    {
		      // CAN transmit successful, toggle green LED
		      HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
		      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);  // Turn off red LED if previously on
		    }

		    // Print all sensor data via UART
		    sprintf(str, "LDR=%u, MQ5=%u, Temp=%.0f C, Hum=%.0f %%\r\n",
		            LDR_DATA, MQ5_DATA, Temperature, Humidity);
		    HAL_UART_Transmit(&huart2, (uint8_t*)str, strlen(str), HAL_MAX_DELAY);

		    HAL_Delay(3000);




    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */
}

